#include <stdio.h>
#include <time.h>

int main(){
  time_t fecha_actual = time(NULL);
  struct tm fecha = *localtime(&fecha_actual);
  const char *month[]={"JAN", "FEB", "MAR", "APR", "MAY", "JUN", "JUL", "AUG", "SEP", "OCT", "NOV", "DIC"};
  const char *day[]={"SUN", "MON", "TUE", "WED", "THU", "FRI", "SAT"};

  printf("Year: %d\n ", fecha.tm_year+1900);
  printf("Month: %s\n ", month[fecha.tm_mon]); //Del 0 al 11
  printf("Month Day: %d\n ", fecha.tm_mday);//Del 0 al 6, Empezando por el domingo
  printf("Hour: %d\n ", fecha.tm_hour);
  printf("Minute: %d\n ", fecha.tm_min);
  printf("Second: %d\n ", fecha.tm_sec);
  printf("Week Day: %s\n ", day[fecha.tm_wday]);
  printf("Year Day: %d\n ", fecha.tm_yday);
    
    return 0;
}






































































































































































