from math import sqrt
print("\n~~~~~~~~~~~~~~~Tetraedro Regular~~~~~~~~~~~~~~~~")
a = int(input("Lado: "))

A = (sqrt(3) * a**2)
V = (sqrt(2) * a**3) / 12
print("Formula Area:  (sqrt(3) * a^2),\t Formula Volumen: (sqrt(2) * a^3) / 12")
print("A = ", A, ",\tV= ", V)
